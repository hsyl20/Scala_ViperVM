package fr.hsyl20.vipervm.platform

import scala.util.continuations._

object Sequence {
  def barrier[T](event:Event):Unit @cps[FutureEvent[T]]= {
    shift { k: (Unit=>FutureEvent[T]) =>
      event.fold(k())
    }
  }
}
